// Gerando operações com JS
let x = 4;
x = x + 8;
console.log(x);
let y = 5;
let soma = x + y;
console.log(soma)

// Cuidar ao trabalhar com strings
let z = "7"
let soma2 = y + z;
console.log(soma2) // o resultado e uma concatenação

// Concatenação é  a operação de unir resultados (**não é somar**)
let nome = "Cloud";
let sobrenome = "Strife";
let uniao = nome + " " + sobrenome; // sinal "+" concatena strings
console.log(uniao)
// pode ser feito dentro do console.log
console.log(nome + " " + sobrenome);
