/*
    Loop "for" é uma estrura de controle que executa determinadas
    vezes um bloco de código.
*/

for (let n = 0/* variável */; n < 10 /* condição */; n = n + 1 /* incremento */) {
    console.log('Frase qualquer ' + n); /* Bloco para execução */
}

for (let i = 1; i <= 25 ; i++) {
    console.log('Repetição ' + i);
}
